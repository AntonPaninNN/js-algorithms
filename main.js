/* Insertion Sort */
function insertionSort(arrayForSort) {
    var n = arrayForSort.length;
    for (var i = 0; i < n; i++) {
        var v = arrayForSort[i], j = i - 1;
        while (j >= 0 && arrayForSort[j] > v) { arrayForSort[j + 1] = arrayForSort[j]; j--; }
        arrayForSort[j + 1] = v;
    }
    return arrayForSort;
}

