/* Bubble Sort */
function bubbleSort(arrayForSort) {
    var n = arrayForSort.length;
    for (var i = 0; i < n - 1; i++) {
        for (var j = 0; j < n - 1; j++) {
            if (arrayForSort[j] > arrayForSort[j + 1]) {
                var temp = arrayForSort[j];
                arrayForSort[j] = arrayForSort[j + 1];
                arrayForSort[j + 1] = temp;
            }
        }
    }
    return arrayForSort;
}

