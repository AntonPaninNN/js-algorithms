/* Selection Sort */
function selectionSort(arrayForSort) {
    var n = arrayForSort.length;
    for (var i = 0; i < n; i++) {
        var min = i;
        for (var j = i; j < n; j++) {
            if (arrayForSort[j] < arrayForSort[min]) {
                min = j;
            }
        }
        var temp = arrayForSort[min];
        arrayForSort[min] = arrayForSort[i];
        arrayForSort[i] = temp;
    }
    return arrayForSort;
}

